    public void mostrarBienvenida(Usuario usuario) {
        String nombreCompleto = "";
        String rol = usuario.getRol();
        
        // Determinar el nombre segÃºn el rol
        switch(rol.toLowerCase()) {
            case "administrador":
                if(usuario.getAdministrador() != null) {
                    nombreCompleto = usuario.getAdministrador().getNombre() +
                            "\n --> Iniciaste Sesion: " + (new Date());
                }
                break;
            case "medico":
                if(usuario.getMedico() != null) {
                    nombreCompleto = usuario.getMedico().getNombre() +
                            "\n --> Iniciaste Sesion: " + (new Date());
                }
                break;
            case "paciente":
                if(usuario.getPaciente() != null) {
                    nombreCompleto = usuario.getPaciente().getNombre() +
                            "\n --> Iniciaste Sesion: " + (new Date());
                }
                break;
        }
        
        usuarioTexto.setText("Bienvenido: " + nombreCompleto);
        rolTexto.setText("Rol: " + rol.substring(0, 1).toUpperCase() + rol.substring(1));
    }
